package com.bal.oop.classes;

public class ClassesAndObjects {
    /*
    The words classes and objects are often used interchangeably. But they are fundamentally different.

    Class
        > a blueprint for creating objects
        For example, we can have a class called car, and in this class we can define everything we need to know about
        a car. A car has attributes(started, currentSpeed...) > Fields. A car can also have behaviors and methods
        (start(), stop() ...)

    Object
        > an instance of a class
        We can create many instances like car1, car2 ... All cars have the same fields and methods. But they  are
        independent of each other. Each object will be in a separate space in memory, and it can be in a separate state

    UML > unified modeling language
     */
}
