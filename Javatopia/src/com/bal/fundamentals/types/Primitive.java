package com.bal.fundamentals.types;

public class Primitive {
    byte age = 30;
    //always use meaningful names for variables
    //underscore to separate every 3 digits (more readable)
    //by default, java sees the numbers as integer
    //better to use a capital L(a lowercase l looks like a 1)
    long viewsCount = 3_123_456_789L;
    float price = 10.99F;
    char letter = 'A';
    boolean isEligible = false;
}
